def get_shader_name_by_int(id: int) -> str:
    hex_id = f"0x{id:08X}"
    return get_shader_name_by_str(hex_id)

def get_shader_name_by_str(id: str) -> tuple[str, str]:
    ms_shader_name = shader_list_ms.get(id)
    msk_shader_name = shader_list_msk.get(id)

    if ms_shader_name and msk_shader_name:
        return ms_shader_name, "ms/k"
    if ms_shader_name and not msk_shader_name:
        return ms_shader_name, "ms"
    if msk_shader_name and not ms_shader_name:
        return msk_shader_name, "k"

    return "unknown", "unknown"

def get_shader_list_as_enum() -> list[tuple[str,str,str]]:
    full_list = {**shader_list_ms, **shader_list_msk}
    out_list: list[tuple[str,str,str]] = []

    for k in full_list:
        name, game = get_shader_name_by_str(k)
        out_list.append((k, f"{name} ({game})", ""))

    return out_list


